package math1;

public class Math01_P480 {
		public static void main(String[] args) {
			
			// Math
			
			// Math는 자바에서 이미 기본으로 제공하는 기능이라
			// import 없이 사용 가능합니다.
			
			
			// .abs()
			int a = -1024;
			System.out.println(Math.abs(a));
			
			// .ceil()
			double b = 3.14;
			System.out.println(Math.ceil(b));
			
			// .floor
			double c = 19.9;
			System.out.println(Math.floor(c));
			
			
			
		}

}
