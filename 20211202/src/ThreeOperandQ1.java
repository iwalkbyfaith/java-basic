
public class ThreeOperandQ1 {
	public static void main(String[] args) {
		// 먼저 int a를 선언하고 여러분들이 원하는 숫자로 초기화해주세요.
		// 그 숫자가 100보다 크다면 "a는 100보다 큽니다."
		// 100보다 작거나 같다면 "a는 100보다 작거나 같습니다."
		// 라고 출력하는 삼항연산자를 아래에 작성해주세요.
		// 웹 개발할때 코드 줄 수를 줄이기 위해서 많이 씀.
		
		int a = 85;
		System.out.println((a > 100? "a는 100보다 큽니다" : "a는 100보다 작거나 같습니다."));
		
		int b = 163;
		System.out.println((b > 100? "b는 100보다 큽니다" : "b는 100보다 작거나 같습니다."));
	}
	}

