
public class DataBool_P36 {
	public static void main(String[] args) {
		// boolean 자료형은 참/거짓을 구분할 때 씁니다.
		// 이 자료형의 변수에는 오직 true/false만 담을 수 있습니다.
		
		boolean a = true;
		boolean b = false;
		// boolean c = 0; (에러)   
		// 다른 언어와 달리 숫자로 참/거짓 표시 금지
		// 다른 언어에서는 0이 false고 1이 true임 ex) 파이썬
		
		
		System.out.println(a);
		System.out.println(b);
	}
}
